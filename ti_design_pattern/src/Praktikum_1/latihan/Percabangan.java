public class Percabangan {
    public static void main(String[] args) {
        int angka = 10;

        if (angka % 2 == 0) {
            System.out.println(angka + " adalah bilangan Genap.");
        } else {
            System.out.println(angka + " adalah bilangan Ganjil.");
        }
    }
}
